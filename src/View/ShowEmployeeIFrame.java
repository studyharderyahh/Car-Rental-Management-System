    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        // Get the selected row
        int selectedRow = EmployeeDetailTable.getSelectedRow();

        // Check if a row is selected
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a row to delete.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        // Get the person ID from the selected row
        String personIdToDelete = (String) EmployeeDetailTable.getValueAt(selectedRow, 0);

        // Display a confirmation dialog
        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this employee?", "Confirm Delete", JOptionPane.YES_NO_OPTION);

        // Check the user's response
        if (confirm == JOptionPane.YES_OPTION) {
            // Remove the selected row from the table model
            model.removeRow(selectedRow);
            HomeForm.person.removeEmployeeById(personIdToDelete);
        }
    }//GEN-LAST:event_deleteButtonActionPerformed


