    private void populateEmployeeTable() {
        model.setRowCount(0);

        ArrayList<Employee> employees = HomeForm.person.getEmployees();

        // Populate the table with employee data
        for (Employee employee : employees) {
            Object[] employeeData = {
                employee.getPersonID(),
                employee.getName(),
                employee.getGender(),
                employee.getAddress(),
                employee.getPhone(),
                employee.getDateOfBirth(),
                employee.getRole(),
                employee.getPhoneExtensionNumber(),
                employee.getUsername(),
                employee.getPassword()
            };
            model.addRow(employeeData);
        }
    }

